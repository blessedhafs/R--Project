#  Temperature Conversion
# Your Task: Create a function to convert Celsius (C) to Fahrenheit (F). You can use the formula:

 Celsius_to_farenheit <- function(C){
   return(C * 1.8 + 3.2)
 }  
 
Celsius_to_farenheit(100)
Celsius_to_farenheit(75)
Celsius_to_farenheit(120)



